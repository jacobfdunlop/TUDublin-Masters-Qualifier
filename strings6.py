s1 = "concord"
s2 = "souix city"
s3 = "HONOLULU"
s4 = "TopHat"

my_list = [s1, s2, s3, s4]

for a in my_list:
    print(a, "capitalize: ", a.capitalize())
    print(a, "swapcase: ", a.swapcase())
    print(a, "upper: ", a.upper())
    print(a, "lower: ", a.lower())
    print(a, "title: ", a.title())
    print()

